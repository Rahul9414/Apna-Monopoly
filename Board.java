
public class Board {
    private String name; // name of the place where player reaches
    private int price; // price a player has to pay when he reaches to a new location
    private int rent; // rent that should be given by another player

    Board(String name, int price, int rent) {
        this.name = name;
        this.price = price;
        this.rent = rent;
    }

    public String get_name() {
        return name;
    }

    public int get_price() {
        return price;
    }

    public int get_rent() {
        return rent;
    }
}
